    private static void AppendOAuthTokenCacheFunctions(StringBuilder sb)
    {
        sb.AppendLine("static string TokenCachePath([CallerFilePath] string callerPath = \"\")");
        sb.AppendLine("{");
        sb.AppendLine("    var scriptDir = Path.GetDirectoryName(callerPath) ?? \".\";");
        sb.AppendLine("    return Path.Combine(scriptDir, \"..\", \".auth-cache.json\");");
        sb.AppendLine("}");
        sb.AppendLine();

        sb.AppendLine("static async Task<Dictionary<string, JsonElement>> ReadTokenCacheAsync(string path)");
        sb.AppendLine("{");
        sb.AppendLine("    var result = new Dictionary<string, JsonElement>(StringComparer.Ordinal);");
        sb.AppendLine("    if (!File.Exists(path)) return result;");
        sb.AppendLine("    try");
        sb.AppendLine("    {");
        sb.AppendLine("        using var doc = JsonDocument.Parse(await File.ReadAllTextAsync(path));");
        sb.AppendLine("        foreach (var prop in doc.RootElement.EnumerateObject()) result[prop.Name] = prop.Value.Clone();");
        sb.AppendLine("    }");
        sb.AppendLine("    catch (JsonException) { }");
        sb.AppendLine("    return result;");
        sb.AppendLine("}");
        sb.AppendLine();

        sb.AppendLine("static async Task WriteTokenCacheAsync(string path, Dictionary<string, JsonElement> cache)");
        sb.AppendLine("{");
        sb.AppendLine("    var obj = new JsonObject();");
        sb.AppendLine("    foreach (var (key, value) in cache) obj[key] = JsonNode.Parse(value.GetRawText());");
        sb.AppendLine("    var tmpPath = path + \".tmp-\" + Guid.NewGuid().ToString(\"N\");");
        sb.AppendLine("    await File.WriteAllTextAsync(tmpPath, obj.ToJsonString(new JsonSerializerOptions { WriteIndented = true }));");
        sb.AppendLine("    if (!OperatingSystem.IsWindows())");
        sb.AppendLine("    {");
        sb.AppendLine("        File.SetUnixFileMode(tmpPath, UnixFileMode.UserRead | UnixFileMode.UserWrite);");
        sb.AppendLine("    }");
        sb.AppendLine("    File.Move(tmpPath, path, overwrite: true);");
        sb.AppendLine("}");
        sb.AppendLine();

        sb.AppendLine("static void StoreTokenInCache(Dictionary<string, JsonElement> cache, string profileName, (string AccessToken, string? RefreshToken, int ExpiresIn) token)");
        sb.AppendLine("{");
        sb.AppendLine("    var obj = new JsonObject { [\"access_token\"] = token.AccessToken, [\"expires_at\"] = DateTimeOffset.UtcNow.AddSeconds(token.ExpiresIn).ToString(\"o\") };");
        sb.AppendLine("    if (token.RefreshToken is not null) obj[\"refresh_token\"] = token.RefreshToken;");
        sb.AppendLine("    cache[profileName] = JsonDocument.Parse(obj.ToJsonString()).RootElement.Clone();");
        sb.AppendLine("}");
        sb.AppendLine();

        sb.AppendLine("static async Task<T> WithTokenCacheLockAsync<T>(Func<Task<T>> action)");
        sb.AppendLine("{");
        sb.AppendLine("    var lockPath = TokenCachePath() + \".lock\";");
        sb.AppendLine("    FileStream? lockStream = null;");
        sb.AppendLine("    for (var attempt = 0; attempt < 100 && lockStream is null; attempt++)");
        sb.AppendLine("    {");
        sb.AppendLine("        try { lockStream = new FileStream(lockPath, FileMode.OpenOrCreate, FileAccess.ReadWrite, FileShare.None); }");
        sb.AppendLine("        catch (IOException) { await Task.Delay(50); }");
        sb.AppendLine("    }");
        sb.AppendLine("    if (lockStream is null)");
        sb.AppendLine("    {");
        sb.AppendLine("        throw new AuthResolutionException(\"Could not acquire the token cache lock (.auth-cache.json.lock) after multiple attempts \\u2014 another process may be stuck holding it.\");");
        sb.AppendLine("    }");
        sb.AppendLine("    try { return await action(); }");
        sb.AppendLine("    finally { lockStream.Dispose(); }");
        sb.AppendLine("}");
        sb.AppendLine();

        sb.AppendLine("static async Task<string> ResolveOAuthAccessTokenAsync(HttpClient http, JsonElement profile, string profileName, JsonElement? secrets)");
        sb.AppendLine("{");
        sb.AppendLine("    var (_, tokenUrl, _) = ResolveOAuthEndpoints(profile);");
        sb.AppendLine("    if (tokenUrl is null) throw new AuthResolutionException($\"Auth profile '{profileName}' (oauth2) has no tokenUrl configured (directly or via a preset).\");");
        sb.AppendLine("    var clientId = ResolveSecretOrLiteral(GetProp(profile, \"clientId\"), secrets);");
        sb.AppendLine("    var grant = GetProp(profile, \"grant\");");
        sb.AppendLine("    var cachePath = TokenCachePath();");
        sb.AppendLine();
        sb.AppendLine("    return await WithTokenCacheLockAsync(async () =>");
        sb.AppendLine("    {");
        sb.AppendLine("        var cache = await ReadTokenCacheAsync(cachePath);");
        sb.AppendLine("        if (cache.TryGetValue(profileName, out var entry))");
        sb.AppendLine("        {");
        sb.AppendLine("            var expiresAt = entry.TryGetProperty(\"expires_at\", out var eaEl) && DateTimeOffset.TryParse(eaEl.GetString(), out var ea) ? ea : DateTimeOffset.MinValue;");
        sb.AppendLine("            var cachedToken = entry.TryGetProperty(\"access_token\", out var atEl) ? atEl.GetString() : null;");
        sb.AppendLine("            if (cachedToken is not null && expiresAt > DateTimeOffset.UtcNow.AddSeconds(30))");
        sb.AppendLine("            {");
        sb.AppendLine("                return cachedToken;");
        sb.AppendLine("            }");
        sb.AppendLine("            var refreshToken = entry.TryGetProperty(\"refresh_token\", out var rtEl) ? rtEl.GetString() : null;");
        sb.AppendLine("            if (refreshToken is not null)");
        sb.AppendLine("            {");
        sb.AppendLine("                var refreshed = await RefreshOAuthTokenAsync(http, profile, tokenUrl, clientId, refreshToken, secrets);");
        sb.AppendLine("                if (refreshed is not null)");
        sb.AppendLine("                {");
        sb.AppendLine("                    StoreTokenInCache(cache, profileName, refreshed.Value);");
        sb.AppendLine("                    await WriteTokenCacheAsync(cachePath, cache);");
        sb.AppendLine("                    return refreshed.Value.AccessToken;");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        if (grant == \"client_credentials\")");
        sb.AppendLine("        {");
        sb.AppendLine("            var fetched = await FetchClientCredentialsTokenAsync(http, profile, tokenUrl, clientId, secrets);");
        sb.AppendLine("            if (fetched is not null)");
        sb.AppendLine("            {");
        sb.AppendLine("                StoreTokenInCache(cache, profileName, fetched.Value);");
        sb.AppendLine("                await WriteTokenCacheAsync(cachePath, cache);");
        sb.AppendLine("                return fetched.Value.AccessToken;");
        sb.AppendLine("            }");
        sb.AppendLine("            throw new AuthResolutionException($\"Failed to obtain a client_credentials token for auth profile '{profileName}'.\");");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        throw new AuthResolutionException($\"No valid or refreshable token for auth profile '{profileName}'. Run: dotnet run scripts/call.cs -- login {profileName}\");");
        sb.AppendLine("    });");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static void AppendOAuthLoginFunction(StringBuilder sb)
    {
        sb.AppendLine("static async Task<int> LoginAsync(string profileName)");
        sb.AppendLine("{");
        sb.AppendLine("    var authConfig = LoadAuthConfig();");
        sb.AppendLine("    var profiles = authConfig is { } cfg && cfg.TryGetProperty(\"profiles\", out var profilesEl) ? profilesEl : (JsonElement?)null;");
        sb.AppendLine("    var profile = FindProfile(profiles, profileName);");
        sb.AppendLine("    if (profile is null)");
        sb.AppendLine("    {");
        sb.AppendLine("        Console.Error.WriteLine($\"No auth profile named '{profileName}' in auth.json.\");");
        sb.AppendLine("        return 2;");
        sb.AppendLine("    }");
        sb.AppendLine("    var type = GetProp(profile.Value, \"type\");");
        sb.AppendLine("    if (type != \"oauth2\")");
        sb.AppendLine("    {");
        sb.AppendLine("        Console.Error.WriteLine($\"Profile '{profileName}' is type '{type}', not 'oauth2' \\u2014 login is only applicable to oauth2 profiles.\");");
        sb.AppendLine("        return 2;");
        sb.AppendLine("    }");
        sb.AppendLine("    var grant = GetProp(profile.Value, \"grant\");");
        sb.AppendLine("    if (grant == \"client_credentials\")");
        sb.AppendLine("    {");
        sb.AppendLine("        Console.WriteLine($\"Profile '{profileName}' uses client_credentials \\u2014 not applicable: no interactive login is needed, a token is obtained automatically at call time.\");");
        sb.AppendLine("        return 0;");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    var (authUrl, tokenUrl, scopes) = ResolveOAuthEndpoints(profile.Value);");
        sb.AppendLine("    if (authUrl is null || tokenUrl is null)");
        sb.AppendLine("    {");
        sb.AppendLine("        Console.Error.WriteLine($\"Profile '{profileName}' is missing authUrl/tokenUrl (directly or via a preset).\");");
        sb.AppendLine("        return 2;");
        sb.AppendLine("    }");
        sb.AppendLine("    var callbackUrl = GetProp(profile.Value, \"callbackUrl\");");
        sb.AppendLine("    if (callbackUrl.Length == 0) callbackUrl = \"http://localhost:8400/callback\";");
        sb.AppendLine("    var secrets = LoadSecrets();");
        sb.AppendLine("    var clientId = ResolveSecretOrLiteral(GetProp(profile.Value, \"clientId\"), secrets);");
        sb.AppendLine();
        sb.AppendLine("    var (verifier, challenge) = GeneratePkce();");
        sb.AppendLine("    var state = GenerateState();");
        sb.AppendLine("    var authorizeUrl = BuildAuthorizeUrl(authUrl, clientId, callbackUrl, scopes, challenge, state, profile.Value);");
        sb.AppendLine();
        sb.AppendLine("    HttpListener callbackListener;");
        sb.AppendLine("    try { callbackListener = BeginCallbackListener(callbackUrl); }");
        sb.AppendLine("    catch (AuthResolutionException ex) { Console.Error.WriteLine(ex.Message); return 2; }");
        sb.AppendLine("    Console.WriteLine($\"Listening for OAuth callback on {callbackUrl} ...\");");
        sb.AppendLine();
        sb.AppendLine("    var browserLaunch = GetProp(profile.Value, \"browserLaunch\");");
        sb.AppendLine("    if (browserLaunch == \"clipboard\")");
        sb.AppendLine("    {");
        sb.AppendLine("        var copied = TryCopyToClipboard(authorizeUrl);");
        sb.AppendLine("        Console.WriteLine(copied");
        sb.AppendLine("            ? $\"Copied the sign-in URL to your clipboard for profile '{profileName}'. Paste it into your browser:\"");
        sb.AppendLine("            : $\"Could not copy the sign-in URL to the clipboard automatically. Open this URL to sign in for profile '{profileName}':\");");
        sb.AppendLine("        Console.WriteLine(authorizeUrl);");
        sb.AppendLine("    }");
        sb.AppendLine("    else");
        sb.AppendLine("    {");
        sb.AppendLine("        Console.WriteLine($\"Opening your browser to sign in for profile '{profileName}'...\");");
        sb.AppendLine("        var browserLaunched = TryLaunchBrowser(authorizeUrl);");
        sb.AppendLine("        Console.WriteLine(browserLaunched");
        sb.AppendLine("            ? \"If it did not open, visit this URL to sign in:\"");
        sb.AppendLine("            : \"Could not launch a browser automatically. Open this URL to sign in:\");");
        sb.AppendLine("        Console.WriteLine(authorizeUrl);");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    Dictionary<string, string> callbackParams;");
        sb.AppendLine("    try { callbackParams = await AwaitOAuthCallbackAsync(callbackListener); }");
        sb.AppendLine("    catch (AuthResolutionException ex) { Console.Error.WriteLine(ex.Message); return 2; }");
        sb.AppendLine();
        sb.AppendLine("    if (!callbackParams.TryGetValue(\"state\", out var returnedState) || returnedState != state)");
        sb.AppendLine("    {");
        sb.AppendLine("        Console.Error.WriteLine(\"The callback's state did not match what was sent \\u2014 rejecting (possible CSRF/mix-up). No token was stored. Run login again.\");");
        sb.AppendLine("        return 2;");
        sb.AppendLine("    }");
        sb.AppendLine("    if (!callbackParams.TryGetValue(\"code\", out var code))");
        sb.AppendLine("    {");
        sb.AppendLine("        var err = callbackParams.TryGetValue(\"error\", out var e) ? e : \"no authorization code was returned\";");
        sb.AppendLine("        Console.Error.WriteLine($\"Login failed: {err}\");");
        sb.AppendLine("        return 2;");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    using var http = new HttpClient();");
        sb.AppendLine("    var tokenResponse = await ExchangeCodeForTokenAsync(http, profile.Value, tokenUrl, clientId, code, verifier, callbackUrl, secrets);");
        sb.AppendLine("    if (tokenResponse is null)");
        sb.AppendLine("    {");
        sb.AppendLine("        Console.Error.WriteLine(\"Failed to exchange the authorization code for a token.\");");
        sb.AppendLine("        return 2;");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    await WithTokenCacheLockAsync(async () =>");
        sb.AppendLine("    {");
        sb.AppendLine("        var cachePath = TokenCachePath();");
        sb.AppendLine("        var cache = await ReadTokenCacheAsync(cachePath);");
        sb.AppendLine("        StoreTokenInCache(cache, profileName, tokenResponse.Value);");
        sb.AppendLine("        await WriteTokenCacheAsync(cachePath, cache);");
        sb.AppendLine("        return true;");
        sb.AppendLine("    });");
        sb.AppendLine();
        sb.AppendLine("    Console.WriteLine($\"Login succeeded for profile '{profileName}'. You can now call operations that use it.\");");
        sb.AppendLine("    return 0;");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    /// <summary>
    /// <c>browserLaunch: "clipboard"</c> (US OAuth manual-browser login): copies the authorize URL
    /// to the system clipboard via native OS tools instead of launching a browser — no new NuGet
    /// dependency, mirroring how <c>TryLaunchBrowser</c> already shells out to <c>open</c>/<c>xdg-open</c>/<c>cmd</c>.
    /// </summary>
    private static void AppendClipboardFunctions(StringBuilder sb)
    {
        sb.AppendLine("static bool RunClipboardCommand(string fileName, string[] args, string text)");
        sb.AppendLine("{");
        sb.AppendLine("    try");
        sb.AppendLine("    {");
        sb.AppendLine("        var psi = new ProcessStartInfo(fileName) { RedirectStandardInput = true, UseShellExecute = false, CreateNoWindow = true };");
        sb.AppendLine("        foreach (var a in args) psi.ArgumentList.Add(a);");
        sb.AppendLine("        using var process = Process.Start(psi);");
        sb.AppendLine("        if (process is null) return false;");
        sb.AppendLine("        process.StandardInput.Write(text);");
        sb.AppendLine("        process.StandardInput.Close();");
        sb.AppendLine("        process.WaitForExit();");
        sb.AppendLine("        return process.ExitCode == 0;");
        sb.AppendLine("    }");
        sb.AppendLine("    catch");
        sb.AppendLine("    {");
        sb.AppendLine("        return false;");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        sb.AppendLine();

        sb.AppendLine("static bool TryCopyToClipboard(string text)");
        sb.AppendLine("{");
        sb.AppendLine("    if (OperatingSystem.IsMacOS()) return RunClipboardCommand(\"pbcopy\", Array.Empty<string>(), text);");
        sb.AppendLine("    if (OperatingSystem.IsWindows()) return RunClipboardCommand(\"clip\", Array.Empty<string>(), text);");
        sb.AppendLine("    if (OperatingSystem.IsLinux())");
        sb.AppendLine("    {");
        sb.AppendLine("        return RunClipboardCommand(\"wl-copy\", Array.Empty<string>(), text)");
        sb.AppendLine("            || RunClipboardCommand(\"xclip\", new[] { \"-selection\", \"clipboard\" }, text)");
        sb.AppendLine("            || RunClipboardCommand(\"xsel\", new[] { \"--clipboard\", \"--input\" }, text);");
        sb.AppendLine("    }");
        sb.AppendLine("    return false;");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static void AppendParseArgsFunction(StringBuilder sb)
    {
        sb.AppendLine("static (Dictionary<string, string> Params, string? Body) ParseArgs(string[] rest)");
        sb.AppendLine("{");
        sb.AppendLine("    var values = new Dictionary<string, string>(StringComparer.Ordinal);");
        sb.AppendLine("    string? body = null;");
        sb.AppendLine("    for (var i = 0; i < rest.Length; i++)");
        sb.AppendLine("    {");
        sb.AppendLine("        if (!rest[i].StartsWith(\"--\", StringComparison.Ordinal) || i + 1 >= rest.Length)");
        sb.AppendLine("        {");
        sb.AppendLine("            continue;");
        sb.AppendLine("        }");
        sb.AppendLine("        var key = rest[i][2..];");
        sb.AppendLine("        var value = rest[++i];");
        sb.AppendLine("        if (key == \"body\") { body = value; } else { values[key] = value; }");
        sb.AppendLine("    }");
        sb.AppendLine("    return (values, body);");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static void AppendDataTables(StringBuilder sb, SkillModel model)
    {
        sb.AppendLine("static Dictionary<string, OperationSpec> BuildOperations() => new()");
        sb.AppendLine("{");
        foreach (var op in EmitterOperations.DistinctByOperationId(model))
        {
            sb.Append("    [").Append(Literal(op.OperationId)).Append("] = new(")
              .Append(Literal(op.Method.Method)).Append(", ")
              .Append(Literal(op.PathTemplate)).Append(", [")
              .Append(string.Join(", ", op.Parameters.Select(p => $"new({Literal(p.Name)}, {Literal(LocationKey(p.In))})")))
              .Append("], ")
              .Append(op.RequestBody is not null ? "true" : "false").Append(", ")
              .Append(op.RequestBody?.ContentType is { } ct ? Literal(ct) : "null").Append(", [")
              .Append(string.Join(", ", op.SecuritySchemeIds.Select(Literal)))
              .Append("], [")
              .Append(string.Join(", ", op.AuthProfileNames.Select(Literal)))
              .Append("]),")
              .AppendLine();
        }
        sb.AppendLine("};");
        sb.AppendLine();

        sb.AppendLine("static Dictionary<string, SchemeSpec> BuildSchemes() => new()");
        sb.AppendLine("{");
        foreach (var scheme in model.SecuritySchemes)
        {
            sb.Append("    [").Append(Literal(scheme.Id)).Append("] = new(")
              .Append(Literal(scheme.Id)).Append(", ")
              .Append(Literal(SchemeKindKey(scheme.Kind))).Append(", ")
              .Append(scheme.ApiKeyName is { } akn ? Literal(akn) : "null").Append(", ")
              .Append(scheme.ApiKeyLocation == ApiKeyLocation.Query ? Literal("query") : scheme.ApiKeyLocation is not null ? Literal("header") : "null").Append(", ")
              .Append(scheme.OAuthTokenUrl is { } url ? Literal(url) : "null")
              .Append("),")
              .AppendLine();
        }
        sb.AppendLine("};");
        sb.AppendLine();

        sb.AppendLine("sealed record ParamSpec(string Name, string Location);");
        sb.AppendLine("sealed record OperationSpec(string Method, string PathTemplate, ParamSpec[] Parameters, bool HasBody, string? BodyContentType, string[] SecuritySchemeIds, string[] AuthProfileNames);");
        sb.AppendLine("sealed record SchemeSpec(string Id, string Kind, string? ApiKeyName, string? ApiKeyLocation, string? OAuthTokenUrl);");
        sb.AppendLine("sealed class AuthResolutionException(string message) : Exception(message);");
    }

    private static string LocationKey(ParameterLocation location) => location switch
    {
        ParameterLocation.Path => "path",
        ParameterLocation.Header => "header",
        _ => "query",
    };

    private static string SchemeKindKey(SecuritySchemeKind kind) => kind switch
    {
        SecuritySchemeKind.ApiKey => "apiKey",
        SecuritySchemeKind.Bearer => "bearer",
        SecuritySchemeKind.Basic => "basic",
        SecuritySchemeKind.OAuth2 => "oauth2",
        _ => "unsupported",
    };

    /// <summary>Renders a C# string literal, escaping backslashes/quotes/control characters.</summary>
    private static string Literal(string value)
    {
        var sb = new StringBuilder(value.Length + 2);
        sb.Append('"');
        foreach (var c in value)
        {
            switch (c)
            {
                case '"': sb.Append("\\\""); break;
                case '\\': sb.Append("\\\\"); break;
                case '\n': sb.Append("\\n"); break;
                case '\r': sb.Append("\\r"); break;
                case '\t': sb.Append("\\t"); break;
                default: sb.Append(c); break;
            }
        }
        sb.Append('"');
        return sb.ToString();
    }
